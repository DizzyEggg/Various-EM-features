enum ball_index item_to_ball(u16 itemID)
{
    if (itemID)
    {
        if (itemID <= LAST_OLD_BALL_ID) //argument is old item
        {
            switch (itemID)
            {
            case ITEM_MASTERBALL:
                return BALL_MASTER;
            case ITEM_POKEBALL:
                return BALL_POKE;
            case ITEM_GREATBALL:
                return BALL_GREAT;
            case ITEM_SAFARIBALL:
                return BALL_SAFARI;
            case ITEM_ULTRABALL:
                return BALL_ULTRA;
            default:
                return itemID - 1; //for other old balls
            }
        }
        if (itemID >= FIRST_NEW_BALL_ID) //itemID is new item
        {
            u16 val = itemID - FIRST_NEW_BALL_ID + 0xC;
            if (val <= 31)
                return val;
        }
    }
    return BALL_POKE; //worst case scenario just return poke ball
}

